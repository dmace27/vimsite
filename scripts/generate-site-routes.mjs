import { readdir, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const appDirectory = path.join(projectRoot, "app");
const outputFile = path.join(projectRoot, "data", "site-routes.generated.ts");
const pageFilePattern = /^page\.(?:js|jsx|ts|tsx)$/;

async function findPageFiles(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const pageFiles = [];

  for (const entry of entries.sort((left, right) => left.name.localeCompare(right.name))) {
    const entryPath = path.join(directory, entry.name);

    if (entry.isDirectory()) {
      pageFiles.push(...(await findPageFiles(entryPath)));
    } else if (pageFilePattern.test(entry.name)) {
      pageFiles.push(entryPath);
    }
  }

  return pageFiles;
}

function humanize(segment) {
  return segment
    .replace(/^\[\[?\.\.\.(.+)\]\]?$/, "$1")
    .replace(/^\[(.+)\]$/, "$1")
    .replace(/[-_]+/g, " ")
    .replace(/\b\w/g, (character) => character.toUpperCase());
}

function routeFromPageFile(pageFile) {
  const relativeFile = path.relative(appDirectory, pageFile);
  const segments = relativeFile.split(path.sep).slice(0, -1);

  // Route groups and parallel route slots do not appear in the public URL.
  const publicSegments = segments.filter(
    (segment) => !(segment.startsWith("(") && segment.endsWith(")")) && !segment.startsWith("@"),
  );

  // Next.js private folders are implementation details, not public pages.
  if (publicSegments.some((segment) => segment.startsWith("_"))) return null;

  const href = publicSegments.length ? `/${publicSegments.join("/")}` : "/";
  const label = publicSegments.length ? humanize(publicSegments.at(-1)) : "Home";
  const dynamic = publicSegments.some((segment) => segment.includes("["));

  return {
    label,
    href,
    detail: `Page · ${href}`,
    dynamic,
  };
}

const routes = (await findPageFiles(appDirectory))
  .map(routeFromPageFile)
  .filter(Boolean)
  .sort((left, right) => {
    if (left.href === "/") return -1;
    if (right.href === "/") return 1;
    return left.href.localeCompare(right.href);
  });

const contents = `// This file is generated by scripts/generate-site-routes.mjs.\n// Add a page under app/**/page.tsx, then run npm run generate:routes.\n\nexport type SiteRoute = {\n  label: string;\n  href: string;\n  detail: string;\n  dynamic: boolean;\n};\n\nexport const siteRoutes = ${JSON.stringify(routes, null, 2)} as const satisfies readonly SiteRoute[];\n`;

await writeFile(outputFile, contents);
console.log(`Generated ${routes.length} site routes in ${path.relative(projectRoot, outputFile)}`);
